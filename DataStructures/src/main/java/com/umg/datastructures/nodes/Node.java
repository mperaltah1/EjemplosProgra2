package com.umg.datastructures.nodes;

/**
 *
 * @author mrph0
 */
public class Node {
    private long id;
    private String name;
    public Node next;

    public Node(long id, String name) {
        this.id = id;
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Node{" + "ID=" + id + ", nombre=" + name + '}';
    }
    
    
}
