    private void btnCalculateSalaryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateSalaryActionPerformed
        int dpi = Integer.parseInt(txtDpi.getText());
        String name = txtName.getText();
        int hours = Integer.parseInt(txtHours.getText());

        EmployeeType type = EmployeeType.valueOfId(cmbEmpType.getSelectedIndex());
        Employee emp;
        switch (type) {
            case MANAGER -> {
                emp = new Manager(dpi, name, hours);
            }
            case EXECUTIVE -> {
                emp = new Executive(dpi, name, hours);
            }
            case DEVELOPER -> {
                emp = new Developer(dpi, name, hours);
            }
            case SECRETARY -> {
                emp = new Secretary(dpi, name, hours);
            }
            default -> {
                emp = null;
            }
        }
        if (emp != null) {
            emp.calculateSalary();
            addToModel(emp);
        }
    }//GEN-LAST:event_btnCalculateSalaryActionPerformed

